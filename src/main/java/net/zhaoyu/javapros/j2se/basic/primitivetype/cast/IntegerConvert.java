package net.zhaoyu.javapros.j2se.basic.primitivetype.cast;

//整数转换为其它进制的数据。
public class IntegerConvert {
	public static void main(String[] args) {
		System.out.printf("%d\n", 26);
		System.out.printf("%d\n", +26);
		System.out.printf("%d\n", -26);
		System.out.printf("%o\n", 26);
		System.out.printf("%x\n", 26);
		System.out.printf("%X\n", 26);
	} // end main
}

