    final V replaceNode(Object key, V value, Object cv) {
        int hash = spread(key.hashCode());
        for (Node<K, V>[] tab = table; ; ) {  //自旋。在表未初始化，或者未找到时退出。
            Node<K,V> f; int n,i,fh;
            if (tab == null || (n = tab.length) == 0 || (f = tabAt(tab, i = (n - 1) & hash))== null) { //初始化f并检查。
                //表未初始化或者未命中节点，则break。
                break;
            } else if ((fh = f.hash) == MOVED) {
                //hash为MOVED表示为ForwardingNode，帮助转移节点。
                tab = helpTransfer(tab, f);
            } else {
                V oldVal=null;
                boolean validated=false;
                synchronized (f) {
                    if (tabAt(tab, i) == f) { //线程安全检查f是否初始时的状态。即是否有并发线程修改了f。
                        if (fh >= 0) { //f为链表节点
                            validated=true;
                            for (Node<K,V> e = f, pred = null;;) {
                                K ek;
                                //找到key
                                if (e.hash == hash && ((ek = e.key) == key || (ek != null && key.equals(ek)))) {
                                    V ev = e.val;
                                    //cv等于null或者cv等于ev。
                                    if (cv == null || cv == ev || (ev != null && cv.equals(ev))) {
                                        oldVal = ev;
                                        if (value != null)
                                            e.val = value; //value不为null,替换操作。
                                        else if (pred != null) //前节点不为null，表示不是头节点。
                                            pred.next = e.next; //前节点的next指向后节点。即删除了当前节点。
                                        else //节点为头节点，直接从tab中删除。
                                            setTabAt(tab, i, e.next);
                                    }
                                    break;
                                }
                                pred = e; // 保存前一个节点。
                                if ((e = e.next) == null) break; //链表循环
                            }

                        }else if (f instanceof TreeBin) { //红黑树处理，逻辑和链表类似。
                            validated = true;
                            TreeBin<K,V> t = (TreeBin<K,V>)f;
                            TreeNode<K,V> r, p;
                            if ((r = t.root) != null &&
                                (p = r.findTreeNode(hash, key, null)) != null) {
                                V pv = p.val;
                                if (cv == null || cv == pv ||
                                    (pv != null && cv.equals(pv))) {
                                    oldVal = pv;
                                    if (value != null)
                                        p.val = value;
                                    else if (t.removeTreeNode(p))
                                        setTabAt(tab, i, untreeify(t.first));
                                }
                            }
                        }
                    }
                }
                if (validated) { //找到节点，结束循环。
                    if (oldVal != null) {
                        if (value == null) //删除节点时，修改计数。
                            addCount(-1L, -1);
                        return oldVal;
                    }
                    break;
                }
            }
        }
        return null;
    }


    /**
     * 增加计数方法。
     * 如果表太小需要扩容，则初始化转移并扩容。如果已经在扩容中，则帮助扩容。
     *
     * @param x 添加的计数
     * @param check if <0, 不检查resize, if <= 1 只在非竞争条件下检查
     */
    private final void addCount(long x, int check) {
        CounterCell[] as; long b, s;
        //如果计数器不为空，或者增加基础计数失败。
        if ((as = counterCells) != null || !U.compareAndSwapLong(this, BASECOUNT, b = baseCount, s = b + x)) {
            CounterCell a; long v; int m;
            boolean uncontended = true;
            //计数器为空 || 计数器长度小于1 || 随机取余一个数组位置为空 || 修改随机取得的槽位失败。
            if (as == null || (m = as.length - 1) < 0 || (a = as[ThreadLocalRandom.getProbe() & m]) == null ||
                !(uncontended = U.compareAndSwapLong(a, CELLVALUE, v = a.value, v + x))) {
                fullAddCount(x, uncontended);
                return;
            }
            if (check <= 1)
                return;
            s = sumCount();
        }
        if (check >= 0) { //需要检查扩容
            Node<K,V>[] tab, nt; int n, sc;
            //map.size()大于sizeCtl(扩容阈值，非扩容时是size的0.75)
            while (s >= (long)(sc = sizeCtl) && (tab = table) != null && (n = tab.length) < MAXIMUM_CAPACITY) {
                int rs = resizeStamp(n);
                if (sc < 0) { //有其他线程正在扩容。
                    if ((sc >>> RESIZE_STAMP_SHIFT) != rs || sc == rs + 1 || //扩容条件检查，参考helpTransfer方法。
                        sc == rs + MAX_RESIZERS || (nt = nextTable) == null || transferIndex <= 0)
                        break;
                    if (U.compareAndSwapInt(this, SIZECTL, sc, sc + 1)) //可以扩容则进行扩容。
                        transfer(tab, nt);
                }
                // 没有其他线程进行扩容，则设置一个扩容线程。然后进行扩容。
                else if (U.compareAndSwapInt(this, SIZECTL, sc, (rs << RESIZE_STAMP_SHIFT) + 2))
                    transfer(tab, null);
                s = sumCount();
            }
        }
    }

    /**
     * 在resize过程中帮助转移。
     */
    final Node<K,V>[] helpTransfer(Node<K,V>[] tab, Node<K,V> f) {
        Node<K,V>[] nextTab; int sc;
        //f节点为ForwardingNode，且正在发生转移，则帮助转移
        if (tab != null && (f instanceof ForwardingNode) && (nextTab = ((ForwardingNode<K,V>)f).nextTable) != null) {
            int rs = resizeStamp(tab.length);
            while (nextTab == nextTable && table == tab && (sc = sizeCtl) < 0) { //正在转移的条件判断。
                //判断不在resize过程中，结束。<(sc >>> RESIZE_STAMP_SHIFT) != rs> 表示长度发生了变化。
                // <sc == rs + 1>和<sc == rs + MAX_RESIZERS>有bug，参考JDK-8214427，因为rs为正数，而sc在扩展时为负数。
                if ((sc >>> RESIZE_STAMP_SHIFT) != rs || sc == rs + 1 || sc == rs + MAX_RESIZERS || transferIndex <= 0)
                    break;
                if (U.compareAndSwapInt(this, SIZECTL, sc, sc + 1)) { // sizeCtl加1并执行转移。
                    transfer(tab, nextTab);
                    break;
                }
            }
            return nextTab;
        }
        return table;
    }

    /**
     * 预扩容，已适应新添加的数量。
     * @param size 元素的数量（不需要十分精确）
     */
    private final void tryPresize(int size) {
        int c = (size >= (MAXIMUM_CAPACITY >>> 1)) ? MAXIMUM_CAPACITY :
            tableSizeFor(size + (size >>> 1) + 1);
        int sc;
        while ((sc = sizeCtl) >= 0) {
            Node<K,V>[] tab = table; int n;
            if (tab == null || (n = tab.length) == 0) {
                n = (sc > c) ? sc : c;
                if (U.compareAndSwapInt(this, SIZECTL, sc, -1)) {
                    try {
                        if (table == tab) {
                            @SuppressWarnings("unchecked")
                            Node<K,V>[] nt = (Node<K,V>[])new Node<?,?>[n];
                            table = nt;
                            sc = n - (n >>> 2);
                        }
                    } finally {
                        sizeCtl = sc;
                    }
                }
            }
            else if (c <= sc || n >= MAXIMUM_CAPACITY)
                break;
            else if (tab == table) {
                int rs = resizeStamp(n);
                if (sc < 0) {
                    Node<K,V>[] nt;
                    if ((sc >>> RESIZE_STAMP_SHIFT) != rs || sc == rs + 1 ||
                        sc == rs + MAX_RESIZERS || (nt = nextTable) == null ||
                        transferIndex <= 0)
                        break;
                    if (U.compareAndSwapInt(this, SIZECTL, sc, sc + 1))
                        transfer(tab, nt);
                }
                else if (U.compareAndSwapInt(this, SIZECTL, sc, (rs << RESIZE_STAMP_SHIFT) + 2))
                    transfer(tab, null);
            }
        }
    }

    /**
     * 从老表中移动或者复制一些节点到新表中。
     */
    private final void transfer(Node<K,V>[] tab, Node<K,V>[] nextTab) {
        int n = tab.length, stride;
        //stride决定了每个线程转移表的步长，即一个线程可以转移多少个桶。通过cpu计算一个步长。
        if ((stride = (NCPU > 1) ? (n >>> 3) / NCPU : n) < MIN_TRANSFER_STRIDE)
            stride = MIN_TRANSFER_STRIDE; // subdivide range
        if (nextTab == null) {            // initiating
            try {
                @SuppressWarnings("unchecked")
                Node<K,V>[] nt = (Node<K,V>[])new Node<?,?>[n << 1];
                nextTab = nt;
            } catch (Throwable ex) {      // try to cope with OOME
                sizeCtl = Integer.MAX_VALUE;
                return;
            }
            nextTable = nextTab;
            transferIndex = n;
        }
        int nextn = nextTab.length;
        ForwardingNode<K,V> fwd = new ForwardingNode<K,V>(nextTab);
        boolean advance = true;
        boolean finishing = false; // to ensure sweep before committing nextTab
        for (int i = 0, bound = 0;;) { //i指向表当前处理的bin的下标。
            Node<K,V> f; int fh;
            //计算transferIndex的值并设置。旧表需要转移的区间为[nextBound,nextIndex]。
            while (advance) {
                int nextIndex, nextBound;
                if (--i >= bound || finishing) //执行了--i
                    advance = false;
                else if ((nextIndex = transferIndex) <= 0) { //更新nextIndex为最新的transferIndex
                    i = -1;
                    advance = false;
                }
                //从nextIndex向前推移stride个元素。得到nextBound，竞争获取转移的区间。
                else if (U.compareAndSwapInt(this, TRANSFERINDEX, nextIndex,
                        nextBound = (nextIndex > stride ? nextIndex - stride : 0))) {
                    bound = nextBound;
                    i = nextIndex - 1; //更新i
                    advance = false;
                }
            }
            //判断处理完成的逻辑
            if (i < 0 || i >= n || i + n >= nextn) {
                int sc;
                if (finishing) {
                    nextTable = null;
                    table = nextTab;
                    sizeCtl = (n << 1) - (n >>> 1); //sizeCtl控制并发线程的数量。表初始化完成后，设置sizeCtl=2n*0.75。
                    return;
                }
                //对sizeCtl减一
                if (U.compareAndSwapInt(this, SIZECTL, sc = sizeCtl, sc - 1)) {
                    // sizeCtl-2和rs右移16位相等，说明新表长度未变化,resize轮次一致，且该线程是最后一个转移线程。 相反，说明新表长度发生了
                    // 变化，或者线程不是最后一个转移线程。 （因为sizeCtl初始化默认为-1，且上面if中sc-1,所以这里是sc-2）
                    if ((sc - 2) != resizeStamp(n) << RESIZE_STAMP_SHIFT)
                        return;
                    finishing = advance = true;
                    i = n; // 没有结束，继续检查
                }
            }
            else if ((f = tabAt(tab, i)) == null) //位置i处为null，表示没有任何节点，初始化为fwd。
                advance = casTabAt(tab, i, null, fwd);
            else if ((fh = f.hash) == MOVED)  //该位置节点已经迁移。
                advance = true; // already processed
            else {
                synchronized (f) {  //同步转移节点
                    if (tabAt(tab, i) == f) {
                        Node<K,V> ln, hn;
                        if (fh >= 0) { //链表迁移
                            int runBit = fh & n;
                            Node<K,V> lastRun = f;
                            for (Node<K,V> p = f.next; p != null; p = p.next) {
                                int b = p.hash & n;
                                if (b != runBit) {
                                    runBit = b;
                                    lastRun = p;
                                }
                            }
                            if (runBit == 0) {
                                ln = lastRun;
                                hn = null;
                            }
                            else {
                                hn = lastRun;
                                ln = null;
                            }
                            //将所有元素按照ph & n==0条件划分为两部分，移动到新的table中。
                            for (Node<K,V> p = f; p != lastRun; p = p.next) {
                                int ph = p.hash; K pk = p.key; V pv = p.val;
                                if ((ph & n) == 0) //n的长度为2的n次方，所以二进制只有一个是0，判断hash在该为上是0还是1
                                    ln = new Node<K,V>(ph, pk, pv, ln);
                                else
                                    hn = new Node<K,V>(ph, pk, pv, hn);
                            }
                            setTabAt(nextTab, i, ln);
                            setTabAt(nextTab, i + n, hn);
                            setTabAt(tab, i, fwd);
                            advance = true;
                        }
                        else if (f instanceof TreeBin) { //红黑树迁移。和链表类似
                            TreeBin<K,V> t = (TreeBin<K,V>)f;
                            TreeNode<K,V> lo = null, loTail = null;
                            TreeNode<K,V> hi = null, hiTail = null;
                            int lc = 0, hc = 0;
                            for (Node<K,V> e = t.first; e != null; e = e.next) {
                                int h = e.hash;
                                TreeNode<K,V> p = new TreeNode<K,V>
                                    (h, e.key, e.val, null, null);
                                if ((h & n) == 0) {
                                    if ((p.prev = loTail) == null)
                                        lo = p;
                                    else
                                        loTail.next = p;
                                    loTail = p;
                                    ++lc;
                                }
                                else {
                                    if ((p.prev = hiTail) == null)
                                        hi = p;
                                    else
                                        hiTail.next = p;
                                    hiTail = p;
                                    ++hc;
                                }
                            }
                            ln = (lc <= UNTREEIFY_THRESHOLD) ? untreeify(lo) :
                                (hc != 0) ? new TreeBin<K,V>(lo) : t;
                            hn = (hc <= UNTREEIFY_THRESHOLD) ? untreeify(hi) :
                                (lc != 0) ? new TreeBin<K,V>(hi) : t;
                            setTabAt(nextTab, i, ln);
                            setTabAt(nextTab, i + n, hn);
                            setTabAt(tab, i, fwd);
                            advance = true;
                        }
                    }
                }
            }
        }
    }


    /**
     * 使用红黑树替换链表
     */
    private final void treeifyBin(Node<K,V>[] tab, int index) {
        Node<K,V> b; int n, sc;
        if (tab != null) {
            if ((n = tab.length) < MIN_TREEIFY_CAPACITY)
                tryPresize(n << 1);
            else if ((b = tabAt(tab, index)) != null && b.hash >= 0) {
                synchronized (b) {
                    if (tabAt(tab, index) == b) {
                        TreeNode<K,V> hd = null, tl = null;
                        for (Node<K,V> e = b; e != null; e = e.next) {
                            TreeNode<K,V> p = new TreeNode<K,V>(e.hash, e.key, e.val, null, null);
                            if ((p.prev = tl) == null)
                                hd = p;
                            else
                                tl.next = p;
                            tl = p;
                        }
                        setTabAt(tab, index, new TreeBin<K,V>(hd));
                    }
                }
            }
        }
    }

    /**
     * 将一个树重新组装为一个链表。
     */
    static <K,V> Node<K,V> untreeify(Node<K,V> b) {
        Node<K,V> hd = null, tl = null;
        for (Node<K,V> q = b; q != null; q = q.next) {
            Node<K,V> p = new Node<K,V>(q.hash, q.key, q.val, null);
            if (tl == null)
                hd = p;
            else
                tl.next = p;
            tl = p;
        }
        return hd;
    }


    /**
     * 使用sizeCtl初始化表，
     */
    private final Node<K,V>[] initTable() {
        Node<K,V>[] tab; int sc;
        while ((tab = table) == null || tab.length == 0) {
            if ((sc = sizeCtl) < 0) //sizeCtrl<0表示其他线程在进行初始化，线程挂起。
                Thread.yield(); // lost initialization race; just spin
            else if (U.compareAndSwapInt(this, SIZECTL, sc, -1)) {  //初始化成功，将sizeCtl默认设置为-1。
                try {
                    if ((tab = table) == null || tab.length == 0) {
                        int n = (sc > 0) ? sc : DEFAULT_CAPACITY;
                        @SuppressWarnings("unchecked")
                        Node<K,V>[] nt = (Node<K,V>[])new Node<?,?>[n];
                        table = tab = nt;
                        sc = n - (n >>> 2); //初始化完成，更新sizeCtl=n*0.75
                    }
                } finally {
                    sizeCtl = sc;
                }
                break;
            }
        }
        return tab;
    }

    /**
     * 该方法将长度n进行运算，用于sizeCtl的设置。避免了并发ABA问题（无法区分不同长度下的transfer线程）。
     * n的左边0的长度和2的15次方做与运算。即2的15次方加n左边0的长度，返回结果范围为[2的15次方，2的15次方+32]
     */
    static final int resizeStamp(int n) {
        return Integer.numberOfLeadingZeros(n) | (1 << (RESIZE_STAMP_BITS - 1));
    }


